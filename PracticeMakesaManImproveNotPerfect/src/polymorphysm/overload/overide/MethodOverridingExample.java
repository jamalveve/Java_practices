package polymorphysm.overload.overide;

public class MethodOverridingExample {

	public void method1() {
		System.out.println("I am from parent method");
	}

	public void method2() {
		System.out.println("I am from parent method");
	}

	public static void staticmethod4() {
		System.out.println("I am static method from parent method");
	}

}
